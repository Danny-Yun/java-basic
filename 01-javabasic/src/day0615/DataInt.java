package day0615;

public class DataInt {
	public static void main(String[] args) {
		/* 정수형을 나타낼 수 있는 자료형은 크게 4가지다.
		 * byte, short, int, long 형이다.
		 * 변수 선언 시 지정 자료형을 위의 키워드로 지정
		 */
		byte b = (byte) 128;
		short s = 32000;
		int i = 3;
		long l = 4;
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);

		/*
		       자바에서 정수의 2진수 표현 방법은 "부호와 2의 보수" 표현방법을 사용합니다.
           - 정수형은 10진수뿐만 아니라 2진수, 8진수, 16진수 등의 형태도 표현할 수 있습니다.
           - 정수형의 디폴트는 int이므로 long형을 지정하려면 할당할 숫자 뒤에 "L"을 붙여줍니다.
		 */
	
	}

}



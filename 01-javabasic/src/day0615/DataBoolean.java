package day0615;

public class DataBoolean {
	public static void main(String[] args) {
		/*
		 * boolean 자료형은 참/거짓을 구분할 때 쓴다.
		 * 이 자료형의 변수에는 오직 true/false만 담을 수 있다.
		 * 논리형은 다른 데이터 타입과 서로 형 변환(type casting)할 수 없다.
		 */
		
		boolean a = true;
		boolean b = false;
//		boolean c = 0;        // 다른 언어와 달리 숫자로 표시 금지
		
		System.out.println(a);
		System.out.println(b);
	}

}
